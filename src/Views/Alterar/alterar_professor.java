    private void InserirDados(int id){
        Connection con = Conexao.AbrirConexao();
        ProfessorDAO sql = new ProfessorDAO((com.mysql.jdbc.Connection) con);
        List<Professor> lista = new ArrayList<>();
        lista = sql.Pegar_Professor(id);
        
        for (Professor a : lista) {
            id_prof.setText("" + a.getId());
            nome_prof.setText("" + a.getNome());
            email_prof.setText("" + a.getEmail());
            senha_prof.setText("" + a.getSenha());
            
        }
        
        Conexao.FecharConexao((com.mysql.jdbc.Connection) con);
    }
        
    private void AtualizaCombo(){
        Connection con = Conexao.AbrirConexao();
        DisciplinaDAO sql = new DisciplinaDAO((com.mysql.jdbc.Connection) con);
        List<Disciplina> lista = new ArrayList<>();
        lista = sql.ListarComboDisciplina();
        jCB_disc.addItem("");
        
        for (Disciplina b : lista) {
            jCB_disc.addItem(b.getNome());
        }
        Conexao.FecharConexao((com.mysql.jdbc.Connection) con);
    }
