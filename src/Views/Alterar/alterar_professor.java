    private void btn_alt_profActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alt_profActionPerformed
    String codigo = id_prof.getText();
    String nome = nome_prof.getText();
    String email = email_prof.getText();
    String senha = senha_prof.getText();
    String disc = jtf_id_disc.getText();
    
    if (nome.equals("") || email.equals("") || senha.equals("") || disc.equals("")) {
        JOptionPane.showMessageDialog(null, "Nenhum campo pode estar vazio!",
                    "Banco de questões", JOptionPane.WARNING_MESSAGE);
    } else {
        Connection con = Conexao.AbrirConexao();
        ProfessorDAO sql = new ProfessorDAO((com.mysql.jdbc.Connection) con);
        int cod = Integer.parseInt(codigo);
        int id_disc = Integer.parseInt(disc);
        Professor a = new Professor();
        
        a.setId(cod);
        a.setNome(nome);
        a.setEmail(email);
        a.setSenha(senha);
        a.setId_disc(id_disc);
        
        
        sql.Alterar_Professor(a);
        Conexao.FecharConexao((com.mysql.jdbc.Connection) con);

        id_professor.setText("");
        id_prof.setText("");
        nome_prof.setText("");
        email_prof.setText("");
        senha_prof.setText("");
        jtf_id_disc.setText("");
        jCB_disc.setSelectedIndex(0);
        
        JOptionPane.showMessageDialog(null, "Professor alterado com sucesso!",
                    "Banco de questões", JOptionPane.INFORMATION_MESSAGE);   
    }
    }//GEN-LAST:event_btn_alt_profActionPerformed

