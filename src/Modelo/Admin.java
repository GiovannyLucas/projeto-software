package modelo;

public class Admin {

    private int id;
    private String email;
    private String senha;

    public int getId() {
        return id;
    }

    public void setCodigo(int id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }


}
