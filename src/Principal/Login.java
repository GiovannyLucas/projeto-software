    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String home = System.getProperty("user.home");
        String turma = jtf_id_turma.getText();
        
        if (turma.equals("")) {
            JOptionPane.showMessageDialog(null, "Nenhuma turma selecionada!",
                    "Banco de quest√µes", JOptionPane.WARNING_MESSAGE);
        } else {

            File dir = new File(home + "/arqs");        
            File arquivo = new File(home + "/arqs/turma.txt");

            try {

                dir.mkdir();
                arquivo.createNewFile();

                FileWriter fw =  new FileWriter(arquivo, true);
                BufferedWriter bw = new BufferedWriter(fw);
                bw.write(turma);
                bw.close();
                fw.close();

            } catch (IOException e) {

            }

            dispose();
            new TelaAluno().setVisible(true);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btn_Entrar_AdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Entrar_AdminActionPerformed
        Connection con = Conexao.AbrirConexao();
        AdminDAO sql = new AdminDAO(con);
        String email = email_adm.getText();
        String senha = senha_adm.getText();
        
        if (email.equals("") || senha.equals("")) {
            JOptionPane.showMessageDialog(null, "Nenhum campo pode estar vazio!",
                    "Banco de quest√µes", JOptionPane.WARNING_MESSAGE);
            email_adm.setText("");
            senha_adm.setText("");
        } else {
            if (sql.Logar(email, senha) == true) {
                
                dispose();
                new TelaAdmin().setVisible(true);
                
            } else {
                JOptionPane.showMessageDialog(null, "Usu√°rio e/ou Senha incorreto(s)!",
                    "Banco de quest√µes", JOptionPane.ERROR_MESSAGE);
                email_adm.setText("");
                senha_adm.setText("");
            }
        }
    }//GEN-LAST:event_btn_Entrar_AdminActionPerformed

